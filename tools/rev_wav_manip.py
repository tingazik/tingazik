def expand_bit_to_byte(bits: bytes):
    result = b""
    for byte in bits:
        ptr = 0x80
        while ptr > 0:
            if byte & ptr:
                result += b"\xff"
            else:
                result += b"\x00"
            ptr >>= 1
    return result

